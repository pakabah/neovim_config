-- Nvimtree
keymap("n", "<leader>e", ":Neotree toggle<cr>", opts)

-- keymap("n", "<leader>f", "<cmd>Telescope find_files<cr>", opts)
keymap("n", "<leader>f", "<cmd>lua require'telescope.builtin'.find_files(require('telescope.themes').get_dropdown({ previewer = false }))<cr>", opts)
keymap("n", "<leader>/", "<cmd>lua require'telescope.builtin'.current_buffer_fuzzy_find(require('telescope.themes').get_dropdown({ previewer = false }))<cr>", opts)
keymap("n", "<c-t>", "<cmd>Telescope live_grep<cr>", opts)

-- DAP Debugging Key Mappings
-- Continue execution till next breakpoint
keymap('n', '<F5>', ":lua require'dap'.continue()<CR>", opts)
-- Step over the function or method on the current line
keymap('n', '<F10>', ":lua require'dap'.step_over()<CR>", opts)
-- Step into the function or method on the current line
keymap('n', '<F11>', ":lua require'dap'.step_into()<CR>", opts)
-- Step out of the current function or method
keymap('n', '<F12>', ":lua require'dap'.step_out()<CR>", opts)
-- Toggle breakpoint on the current line
keymap('n', '<leader>b', ":lua require'dap'.toggle_breakpoint()<CR>", opts)
-- Set a conditional breakpoint
keymap('n', '<leader>B', ":lua require'dap'.set_breakpoint(vim.fn.input('Breakpoint condition: '))<CR>", opts)
-- Open REPL window
keymap('n', '<leader>dr', ":lua require'dap'.repl.open()<CR>", opts)
-- Run the last debug configuration
keymap('n', '<leader>dl', ":lua require'dap'.run_last()<CR>", opts)

keymap('n', '<leader>ss', ":lua start_server()<CR>", opts)

keymap('n', '<leader>sm', ":lua run_metadata()<CR>", opts)
